export function getTranslation(localeCode: string | null, key: string): string {
  if (!localeCode) return key
  // Extract language code e.g. "fr" from "fr-FR" or "fr"
  const lang = localeCode.split("-")[0].toLowerCase()
  const dictionary = translations[lang]
  if (dictionary && dictionary[key] !== undefined) {
    return dictionary[key]
  }
  return key
}
